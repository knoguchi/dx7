fn gen_sintab() {
    const N: usize = 1024;
    let mut tab = [0i32; N << 1];

    let dphase = 2.0 * PI / N as f64;
    let c = (dphase.cos() * (1i64 << 30) as f64 + 0.5).floor() as i32;
    let s = (dphase.sin() * (1i64 << 30) as f64 + 0.5).floor() as i32;
    let r: i64 = 1 << 29;
    let mut u: i32 = 1 << 30;
    let mut v: i32 = 0;

    for i in 0..(N / 2) {
        tab[(i << 1) + 1] = (v + 32) >> 6;
        tab[((i + N / 2) << 1) + 1] = -((v + 32) >> 6);
        let t = ((u as i64 * s as i64 + v as i64 * c as i64 + r) >> 30) as i32;
        u = ((u as i64 * c as i64 - v as i64 * s as i64 + r) >> 30) as i32;
        v = t;
    }

    for i in 0..(N - 1) {
        tab[i << 1] = tab[(i << 1) + 3] - tab[(i << 1) + 1];
    }
    tab[(N << 1) - 2] = -tab[(N << 1) - 1];

    print_i32_array("SINTAB", &tab);
}

fn gen_exp2tab() {
    const N: usize = 1024;
    let mut tab = [0i32; N << 1];

    let inc = (1.0f64 / N as f64).exp2();
    let mut y: f64 = (1u64 << 30) as f64;

    for i in 0..N {
        tab[(i << 1) + 1] = (y + 0.5).floor() as i32;
        y *= inc;
    }

    for i in 0..(N - 1) {
        tab[i << 1] = tab[(i << 1) + 3] - tab[(i << 1) + 1];
    }
    tab[(N << 1) - 2] =
        ((1u32 << 31).wrapping_sub(tab[(N << 1) - 1] as u32)) as i32;

    print_i32_array("EXP2TAB", &tab);
}

fn gen_sinlog_table() {
    const N: usize = 1024;
    let mut tab = [0u16; N];

    for i in 0..N {
        let x = ((0.5 + i as f64) / N as f64 * PI / 2.0).sin();
        tab[i] = (-1024.0 * x.log2()).round() as u16;
    }

    print_u16_array("SINLOG_TABLE", &tab);
}

fn gen_sinexp_table() {
    const N: usize = 1024;
    let mut tab = [0u16; N];

    for i in 0..N {
        let x = ((i as f64 / N as f64).exp2() - 1.0) * 4096.0;
        tab[i] = x.round() as u16;
    }

    print_u16_array("SINEXP_TABLE", &tab);
}

